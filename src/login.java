    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
      
        String enterpw = password.getText();
  
        String passwordToHash = enterpw;
        String generatedPassword = null;
        try {

            MessageDigest md = MessageDigest.getInstance("MD5");

            md.update(passwordToHash.getBytes());

            byte[] bytes = md.digest();

            StringBuilder sb = new StringBuilder();
            for(int i=0; i< bytes.length ;i++)
            {
                sb.append(Integer.toString((bytes[i] & 0xff) + 0x100, 16).substring(1));
            }

            generatedPassword = sb.toString();
        } 
        catch (Exception e) 
        {
            e.printStackTrace();
        }
        
        
        try{
            
            stmt = conn.createStatement();
            String Email = mail.getText();
            
            String sql = "SELECT * FROM admin WHERE Email='"+Email+"' && password = '"+generatedPassword+"'";
            
            rs = stmt.executeQuery(sql);
            if(rs.next()){ 
                setVisible(false);
                home object = new home();
                object.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null,"Password or mail is invalid", "Error", JOptionPane.ERROR_MESSAGE);
            }
        
            
        }catch(HeadlessException | SQLException e){ 
            JOptionPane.showMessageDialog(null,e);
        }
        
        
    }//GEN-LAST:event_loginActionPerformed

