    private void mailFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_mailFocusGained
        
        if (mail.getText().trim().equals("Username")) {
            mail.setText(null);
            mail.setForeground(new Color(0, 0, 0));
        }

     
    }//GEN-LAST:event_mailFocusGained

    private void mailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_mailFocusLost
        
        if (mail.getText().trim().equals("")) {
            mail.setText("Username");
            mail.setForeground(new Color(195, 195, 195));
        }
     
    }//GEN-LAST:event_mailFocusLost

    private void passwordFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordFocusGained

       if (new String(password.getPassword()).trim().equals("Password")) {
            password.setText(null);
            password.setEchoChar('*');
            password.setForeground(new Color(0, 0, 0));
        }
    }//GEN-LAST:event_passwordFocusGained

    private void passwordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordFocusLost

        if (new String(password.getPassword()).equals("")) {
            password.setEchoChar((char) 0);
            password.setForeground(new Color(195, 195, 195));
            password.setText("Password");
        }
    }//GEN-LAST:event_passwordFocusLost

